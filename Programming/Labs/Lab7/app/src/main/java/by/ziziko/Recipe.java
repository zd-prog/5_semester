package by.ziziko;

public class Recipe {

    public String name;
    public String category;
    public String ingredients;
    public String recipe;
    public String time;

    Recipe(String name, String category, String ingredients, String recipe, String time)
    {
        this.name = name;
        this.category = category;
        this.ingredients = ingredients;
        this.recipe = recipe;
        this.time = time;
    }
}
