package by.ziziko;

public class Book {

    public String name;
    public  String author;
    public String data;
    public String genre;
    public  String date;

    Book(String name, String author, String data, String genre, String date)
    {
        this.name = name;
        this.author = author;
        this.data = data;
        this.genre = genre;
        this.date = date;
    }
}
